function solution(board) {
    let answer = 0;
    let dx = [-1, 0, 1, 1];
    let dy = [1, 1, 1, 0];

    let pan = Array.from(Array(21), ()=>Array(21).fill(0));
    
    for(let i = 1; i <= 19; i++) {
        for(let j = 1; j <= 19; j++) {
            pan[i][j] = board[i-1][j-1];
        }
    }

    for(let k = 0; k < 19; k++) {
        for(let r = 0; r < 19; r++) {
            if(pan[k][r] != 0) { 
                for(let l = 0; l < 4; l++) {
                    let x = k; //시작할 돌의 좌표
                    let y = r; //시작할 돌의 좌표
                    let cnt = 1;
                    if(pan[k-dx[l]][r-dy[l]] != pan[k][r]) {
                        while(true) {
                            x = x+dx[l];
                            y = y+dy[l];
                            if(pan[x][y] == pan[k][r]) cnt += 1;
                            else break;
                        }
                    }
                    if(cnt === 5) {
                        answer = pan[k][r];
                        return answer;
                    }
                }
            }
        }
    }
}

